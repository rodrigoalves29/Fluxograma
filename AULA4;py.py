# declaração de variavel
# int
numero = 10
tipo0 = type (numero)
print(tipo0)

# float
numero_real = 1.5
tipo1 = type (numero_real)
print(tipo1)

# string, texto
texto = 'Bom dia'
tipo2 = type (texto)
print(tipo2)

# boleano
verdadeiro = True
falso = False
tipo3 = type (verdadeiro)
tipo4 = type (falso)
print(tipo3)
print(tipo4)
